#ifndef CRYPTO_UTILS_H
#define CRYPTO_UTILS_H

#include <string>
/**
 * Шифрует файл с использованием алгоритма AES-256-CBC.
 *
 * @param inputPath Путь к входному файлу, который необходимо зашифровать.
 * @param outputPath Путь к выходному файлу, в который будет сохранён зашифрованный файл.
 * @param password Пароль, используемый для получения ключа шифрования и вектора инициализации (IV).
 * @return Возвращает true, если файл был успешно зашифрован, в противном случае — false.
 */
bool encryptFile(const std::string& inputPath, const std::string& outputPath, const std::string& password);
/**
 * Расшифровывает зашифрованный файл с использованием AES-256-CBC и записывает расшифрованное содержимое в указанный файл.
 *
 * Эта функция использует пароль для получения ключа шифрования и вектора инициализации (IV)
 * для расшифровки с помощью AES-256-CBC. Входной файл читается по блокам, расшифровывается
 * и записывается в выходной файл. Если расшифровка не удаётся (например, из-за неверного пароля
 * или повреждённых данных), функция возвращает false.
 *
 * @param inputPath Путь к зашифрованному входному файлу.
 * @param outputPath Путь к выходному файлу, в который будет записано расшифрованное содержимое.
 * @param password Пароль, используемый для получения ключа и вектора инициализации при расшифровке.
 * @return true, если расшифровка прошла успешно, в противном случае — false.
 */
bool decryptFile(const std::string& inputPath, const std::string& outputPath, const std::string& password);

#endif